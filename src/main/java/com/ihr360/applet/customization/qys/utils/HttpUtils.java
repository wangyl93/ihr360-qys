    /**
     * 向指定 URL 发送POST方法的请求
     *
     * @param url			发送请求的 URL
     * @param param 		请求参数，请求参数应该是 name1=value1&name2=value2 的形式。
     * @param authorization	setRequestProperty authorization值:获取access_token输入" Bearer "+Base64Util(AppID+":"+AppSecret));获取其他数据接口输入 " Bearer "+access_token
     * @return 所代表远程资源的响应结果
     * @throws UnsupportedEncodingException
     */
    public static String sendPost1(String url, String param,String authorization) throws UnsupportedEncodingException {
        OutputStreamWriter out = null;
        BufferedReader in = null;
        HttpURLConnection conn = null;
        String result = "";
        try {
            URL realUrl = new URL(url);
            conn = (HttpURLConnection) realUrl.openConnection();

            // 发送POST请求必须设置如下两行
            conn.setDoOutput(true);
            conn.setDoInput(true);
            conn.setRequestMethod("POST"); //POST方法
            // 设置通用的请求属性
            conn.setRequestProperty("Content-Type","application/json;charset=UTF-8");
            conn.setRequestProperty("Authorization",authorization);

            conn.connect();
            // 获取URLConnection对象对应的输出流
            out = new OutputStreamWriter(conn.getOutputStream(),"UTF-8");
            // 发送请求参数
            out.write(param);
            // flush输出流的缓冲
            out.flush();
            Integer code = conn.getResponseCode();
            // 定义BufferedReader输入流来读取URL的响应
            if(code == 500){
                in = new BufferedReader(new InputStreamReader(conn.getErrorStream(),"UTF-8"));
            } else {
                in = new BufferedReader(new InputStreamReader(conn.getInputStream(),"UTF-8"));
            }
            String line;
            while ((line = in.readLine()) != null) {
                result += line;
            }

        } catch (Exception e) {
            System.out.println("发送 POST 请求出现异常！" + e);
            e.printStackTrace();
        }
        // 使用finally块来关闭输出流、输入流
        finally {
            try {
                if (out != null) {
                    out.close();
                }
                if (in != null) {
                    in.close();
                }
                //断开连接
                conn.disconnect();
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
        return result;
    }

    /**
     * 原生字符串发送post请求
     *
     * @param url
     * @param jsonStr
     * @return
     * @throws IOException
     */
    public static String doPost(String url, String token, String jsonStr) {
        log.info("url:"+url);
        log.info("params:"+jsonStr);
        CloseableHttpClient httpClient = HttpClients.createDefault();
        HttpPost httpPost = new HttpPost(url);
        RequestConfig requestConfig = RequestConfig.custom().setConnectTimeout(35000).setConnectionRequestTimeout(35000).setSocketTimeout(60000).build();
        httpPost.setConfig(requestConfig);
        // 首先Header部分需要设定字符集为：uft-8
        httpPost.addHeader("Content-Type", "application/json;charset=utf-8");
        // 此处也需要设定
        httpPost.setHeader("Accept", "application/json,text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8");
        httpPost.setHeader("Accept-Encoding", "gzip, deflate");
        httpPost.setHeader("Accept-Language", "zh-cn,zh;q=0.8,en-us;q=0.5,en;q=0.3");
        httpPost.setHeader("Connection", "keep-alive");
        httpPost.setHeader("User-Agent", "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/78.0.3904.97 Safari/537.36");
        httpPost.setHeader("Authorization", token);

        CloseableHttpResponse httpResponse = null;
        try {
            httpPost.setEntity(new StringEntity(jsonStr,"utf-8"));
            httpResponse = httpClient.execute(httpPost);
            HttpEntity entity = httpResponse.getEntity();
            String result = EntityUtils.toString(entity);
            log.info("result:"+result);
            return result;
        } catch (ClientProtocolException e) {
            // TODO Auto-generated catch block
            log.info("ClientProtocolException:"+e.getMessage(),e);
        } catch (IOException e) {
            // TODO Auto-generated catch block
            log.info("IOException:"+e.getMessage(),e);
        } finally {
            if (httpResponse != null) {
                try {
                    httpResponse.close();
                } catch (IOException e) {
                    // TODO Auto-generated catch block
                    log.info("IOException:"+e.getMessage(),e);
                }
            }
            if (null != httpClient) {
                try {
                    httpClient.close();
                } catch (IOException e) {
                    log.info("IOException:"+e.getMessage(),e);
                }
            }
        }
        return null;
    }



